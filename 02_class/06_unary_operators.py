# Unary Operator there are two types

# (1) -> Increment operator 
# a++ means ->> Post Increment operator [Kaam , Update]
# ++a means ->> Pre Increment operator [Update , Kaam]

# (2) -> Decrement operator -->> a
# a-- means ->> Post Decrement operator [Kaam , Update]
# --a means ->> Pre Decrement operator [Update , Kaam]

print("(1) -> Increment operator")

a = 10
b = a+1
print("a = ",a)
print("b = ",b)

a = 10
b = 1+a
print("a = ",a)
print("b = ",b)

print("(2) -> Decrement operator")

c = 10
d = c-1
print("c = ",c)
print("d = ",d)

c = 10
d = 1-c
print("c = ",c)
print("d = ",d)
