marks=int(input("Enter a Marks:-"))
if marks>=90:
    print("A - Grade")
elif marks>=80 and marks<90:
    print("B - Grade")
elif marks>=70 and marks<80:
    print("C - Grade")
elif marks>=60 and marks<70:
    print("D - Grade")
elif marks>=30 and marks<60:
    print("E - Grade")
else:
    print("F - Grade") 